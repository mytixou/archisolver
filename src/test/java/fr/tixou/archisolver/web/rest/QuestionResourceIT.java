    @Test
    @Transactional
    void partialUpdateQuestionWithPatch() throws Exception {
        // Initialize the database
        questionRepository.saveAndFlush(question);

        int databaseSizeBeforeUpdate = questionRepository.findAll().size();

        // Update the question using partial update
        Question partialUpdatedQuestion = new Question();
        partialUpdatedQuestion.setId(question.getId());

        partialUpdatedQuestion.designation(UPDATED_DESIGNATION).explication(UPDATED_EXPLICATION).typeQuestion(UPDATED_TYPE_QUESTION);

        restQuestionMockMvc
            .perform(
                patch(ENTITY_API_URL_ID, partialUpdatedQuestion.getId())
                    .contentType("application/merge-patch+json")
                    .content(TestUtil.convertObjectToJsonBytes(partialUpdatedQuestion))
            )
            .andExpect(status().isOk());

        // Validate the Question in the database
        List<Question> questionList = questionRepository.findAll();
        assertThat(questionList).hasSize(databaseSizeBeforeUpdate);
        Question testQuestion = questionList.get(questionList.size() - 1);
        assertThat(testQuestion.getDesignation()).isEqualTo(UPDATED_DESIGNATION);
        assertThat(testQuestion.getExplication()).isEqualTo(UPDATED_EXPLICATION);
        assertThat(testQuestion.getTypeQuestion()).isEqualTo(UPDATED_TYPE_QUESTION);
    }

